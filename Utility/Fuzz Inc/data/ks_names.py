import hashlib
import string

_ALPHABET = string.digits + string.ascii_lowercase


def _h(name: str) -> str:
    digest = hashlib.md5(name.encode("utf-8")).digest()
    bits = int.from_bytes(digest, "big")
    result = ""
    for i in range(3, 3 + 128, 5):
        result += _ALPHABET[(bits << i >> 128) & 0x1F]
    return result


